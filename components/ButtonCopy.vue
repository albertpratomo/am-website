<script setup lang="ts">
interface Props {
    content: string
}

const props = defineProps<Props>()

const copied = ref(false)

function copyText() {
    copied.value = true

    navigator.clipboard.writeText(props.content)

    setTimeout(() => copied.value = false, 1000)
}
</script>

<template>
    <Button
        size="icon"
        variant="secondary"
        :class="{ '!bg-accent': copied }"
        @click="copyText()"
    >
        <svg v-if="!copied" xmlns="http://www.w3.org/2000/svg" width="32" height="32" viewBox="0 0 24 24"><path fill="none" stroke="currentColor" stroke-linecap="round" stroke-linejoin="round" stroke-miterlimit="10" stroke-width="1.5" d="M14 3a2 2 0 1 1-4 0m4 0a2 2 0 1 0-4 0m4 0h4.6A2.4 2.4 0 0 1 21 5.4v15.2a2.4 2.4 0 0 1-2.4 2.4H5.4A2.4 2.4 0 0 1 3 20.6V5.4A2.4 2.4 0 0 1 5.4 3H10" /></svg>

        <svg v-else xmlns="http://www.w3.org/2000/svg" width="32" height="32" viewBox="0 0 24 24"><path fill="none" stroke="currentColor" stroke-linecap="round" stroke-linejoin="round" stroke-width="1.5" d="M14 3a2 2 0 1 1-4 0m4 0a2 2 0 1 0-4 0m4 0h4.6A2.4 2.4 0 0 1 21 5.4v15.2a2.4 2.4 0 0 1-2.4 2.4H5.4A2.4 2.4 0 0 1 3 20.6V5.4A2.4 2.4 0 0 1 5.4 3H10M8 14l3 3l5-7" /></svg>
    </Button>
</template>
